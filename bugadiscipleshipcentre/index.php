    <h2>Buga Discipleship Centre &amp; Farm</h2>
<p>
The progress of the ministry has been quite outstanding.  There are three emphases of the whole "Buga Discipleship Centre" (Buga is the Kachin reference to "Local Church").
</p>
<ol>
<li> Ministry to Drug Addicts -- a one-year program for detoxification of the addict and instruction in the Bible and learning a skill that they can use to support themselves after they leave the farm.  They currently have about 15 recovering addicts on the farm and have ministered to over 120 in the last few years. </li>
<li> Ministry to Youth -- a retreat for youth (3 weeks or 6 weeks) to help them get grounded in the Word before they become addicts â€“ kind of a preventative effort.  They have about 500 youth each summer that come to the farm at various times. </li>
<li>School of Mission -- and effort to further train men &amp; women who have been reaching out to the tribes along the Myanmar-China border.  The idea is to equip many to help start churches among the tribes that have historically made their living from opium in Myanmar &amp; China.  Those churches will become like a "beachhead" for evangelistic efforts among those tribes and hopefully reduce the trafficking of opium in that area by changing the hearts of the people. </li>
</ol>
<p>
